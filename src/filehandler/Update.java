    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:4
        String ssn = EID.getText();
        Admin d = new Admin();
        String name = Name.getText();
        String ssn1 = Ssn.getText();
        String email = EMail.getText();
        String address = Address.getText();
        String region = Region.getText();
        String user = UserName.getText();
        String pass = Password.getText();
        String meter = Meter_code.getText();
        if (jComboBox1.getSelectedItem().equals("Customer")) {
            Old_Customer oc = new Old_Customer();

            oc.setName(name);
            oc.setPassword(pass);
            oc.setAddress(address);
            oc.setEmail(email);
            oc.setUsername(user);
            oc.setMeterCode(meter);
            oc.setRegion(region);
            oc.setSsn(ssn1);
            d.Update(ssn,"Customer",oc);
            JOptionPane.showMessageDialog(null, "Ubdated successfully");
        } else if (jComboBox1.getSelectedItem().equals("Operator")) {
            Operator op = new Operator();

            op.setName(name);
            op.setOp_Ssn(ssn1);
            op.setPassword(pass);
            op.setUsername(user);
            d.Update(ssn,"Operator",op);
            JOptionPane.showMessageDialog(null, "Ubdated successfully");            
        } else if (jComboBox1.getSelectedItem().equals("Admin")) {
            Admin a = new Admin();

            a.setName(name);
            a.setA_Ssn(ssn1);
            a.setPassword(pass);
            a.setUsername(user);
            d.Update(ssn,"Admin",a);
            JOptionPane.showMessageDialog(null, "Ubdated successfully");

        }
        Name.setText("");
        Ssn.setText("");
        EMail.setText("");
        Address.setText("");
        Region.setText("");
        UserName.setText("");
        Password.setText("");
        Meter_code.setText("");


    }//GEN-LAST:event_UpdateActionPerformed


