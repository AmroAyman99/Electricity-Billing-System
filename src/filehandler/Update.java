    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String ssn = EID.getText();
        Admin d = new Admin();
        if (jComboBox1.getSelectedItem().equals("Customer")) {
            Old_Customer oc = new Old_Customer();
            oc = (Old_Customer) d.Search(ssn, "Customer");
            if (oc.getPassword().equals(-1 + "")) {
                JOptionPane.showMessageDialog(null, "Customer not founded");
            } else {
                Name.setText(oc.getName());
                Ssn.setText(oc.getSsn());
                EMail.setText(oc.getEmail());
                Address.setText(oc.getAddress());
                Region.setText(oc.getRegion());
                UserName.setText(oc.getUsername());
                Password.setText(oc.getPassword());
                Meter_code.setText(oc.getMeterCode());
            }
        } else if (jComboBox1.getSelectedItem().equals("Operator")) {
            Operator op = new Operator();
            op = (Operator) d.Search(ssn, "Operator");
            if (op.getPassword().equals(-1 + "")) {
                JOptionPane.showMessageDialog(null, "Operator not founded");
            } else {
                Name.setText(op.getName());
                Ssn.setText(op.getOp_Ssn());
                UserName.setText(op.getUsername());
                Password.setText(op.getPassword());
            }

        } else if (jComboBox1.getSelectedItem().equals("Admin")) {
            Admin a = new Admin();
            a = (Admin) d.Search(ssn, "Admin");
            if (a.getPassword().equals(-1 + "")) {
                JOptionPane.showMessageDialog(null, "Admin not founded");
            } else {
                Name.setText(a.getName());
                Ssn.setText(a.getA_Ssn());
                UserName.setText(a.getUsername());
                Password.setText(a.getPassword());
            }

        }

    }//GEN-LAST:event_SearchActionPerformed

